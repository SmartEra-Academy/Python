# Arithmetic operators are used to perform mathematical operations 

a = 10
b = 4

print(a + b)   # Addition
print(a - b)   # Subtraction
print(a * b)   # Multiplication
print(a / b)   # Division
print(a // b)  # Floor Division
print(a % b)   # Modulus (remainder)
print(a ** b)  # Exponentiation (power)
