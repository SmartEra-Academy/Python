# Membership operators 

# list
my_list = [1, 2, 3, 4, 5]

# Check if 3 is in the list
print(3 in my_list)

# Check if 6 is in the list
print(6 in my_list) 

# Check if 7 is not in the list
print(7 not in my_list) 

# string
my_string = "Hello, World!"

# Check if 'H' is in the string
print('H' in my_string) 

# Check if 'Z' is in the string
print('Z' in my_string) 

# Check if 'Hello' is in the string
print('Hello' in my_string) 

# Check if 'F' is not in the string
print('F' not in my_string) 

