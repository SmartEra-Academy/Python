# Replace parts of a string

text = "Hello, World!"
new_text = text.replace("World", "Python")
print(new_text)

text_2 = "Hello, World!"
text_2 = text_2.replace("e", "a")
print(text_2)

text_3 = "This is a cat"
print(text_3.replace("s", "z")) 

text_4 = "This is a cat and that is a cat"
print(text_4.replace("cat", "dog")) 